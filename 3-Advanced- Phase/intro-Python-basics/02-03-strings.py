#Advanced string

message = "hello"
print(message[0])#first character
print(message[1])
print(message[-2])#second last character
print(len(message))#length of string
print(message.lower())#convert all characters to lower case
print(message.upper())#convert all characters to upper case
print(message.replace("h", "H"))#replace h with H
print(message.strip())#remove leading and trailing spaces
print(message.split())#split the string into a list based on the space character