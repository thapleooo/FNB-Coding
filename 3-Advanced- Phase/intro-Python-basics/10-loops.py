#Loops(for, while)

#for loop (using the for loop to iterate over a list of names)
names = ["Alice", "Bob", "Charlie"]

for name in names:
  print(name)

numbers = [1, 2, 8, 12, 20]
for number in numbers:
  print(number)


#while loop (unsing the while loop to count from 1 to 6)
count = 1

while count <= 6:
  print(count)
  count = count + 1 #incrementing the count variable by 1