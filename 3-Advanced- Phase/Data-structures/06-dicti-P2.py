#Operators

#Addition(+)
#Subtraction(-)
#Multiplication(*)
#Division(/)
#Modulus(%)
#Exponent(**)

x = 20
y = 5
# Addition
print(x + y) 
# Subtraction
print(x - y)  
# Multiplication
print(x * y) 
# Division
print(x / y)  
# Modulus
print(x % y) 
print(5 % 2) 
# Exponent
print(x ** y)  

# Assignment Operators
x = 20
x = x + 5
print(x) 
y = 20
y += 5  # This is equivalent to y = y + 5
print(y) 
