fruits = ["apple", "banana", "cherry"]

fruits.append("orange")  #adding an element to the end of the list
print(fruits)

fruits.insert(2, "kiwi")  #inserting an element at a specific position
print(fruits)   

fruits.remove("banana")  #removing an element by value
print(fruits)

fruits.sort()
print(fruits)  #sorting the list in ascending order

fruits.reverse()  #reversing the order of the list
print(fruits)  #printing the reversed list

fruits.pop()  #removing the last element
print(fruits)  #printing the list after popping the last element
