# Dictionaries
# A dictionary is a collection of key-value pairs.
# Dictionaries are written with curly brackets, with keys and values separated by a colon.

my_dict = {'Apple': 5, 'Banana': 3, 'Cherry': 8}
print(my_dict)  #printing the dictionary
print(my_dict['Banana'])  #accessing the value associated with the key 'Banana'
my_dict['Banana'] = 4  #modifying the value associated with the key 'Banana'
print(my_dict)  #printing the modified dictionary
my_dict['Orange'] = 6  #adding a new key-value pair
print(my_dict)  #printing the dictionary after adding a new key-value pair
